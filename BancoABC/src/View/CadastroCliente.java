    private void jButtonSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarClienteActionPerformed
   
        jTextFieldPesquisaCliente.setText("");
        if (jTextFieldNomeCliente.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "PREENCHA O CAMPO 'NOME' PARA CONTINUAR!");
           jTextFieldNomeCliente.requestFocus();
       }else if(jTextFieldContactoCliente.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "PREENCHA O CAMPO 'CONTACTO' PARA CONTINUAR!");
           jTextFieldContactoCliente.requestFocus();
       }else if(jTextFieldIdadeCliente.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "PREENCHA O CAMPO 'IDADE' PARA CONTINUAR!");
           jTextFieldIdadeCliente.requestFocus();   
           }else if(jTextFieldMoradaCliente.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "PREENCHA O CAMPO 'MORADA' PARA CONTINUAR!");
           jTextFieldMoradaCliente.requestFocus(); 
           
           } else
           
        if (flag==1){
            
        cliente.setNome(jTextFieldNomeCliente.getText());
        cliente.setContacto(jTextFieldContactoCliente.getText());
        cliente.setIdade(Integer.parseInt(jTextFieldIdadeCliente.getText()));
        cliente.setSexo((String)jComboBoxSexoCliente.getSelectedItem());
        cliente.setMorada(jTextFieldMoradaCliente.getText());
        control.salvar(cliente);
        
        
        jTextFieldCodigoCliente.setText("");
        jTextFieldContactoCliente.setText("");
        jTextFieldIdadeCliente.setText("");
        jTextFieldMoradaCliente.setText("");
        jTextFieldNomeCliente.setText("");
        jTextFieldPesquisaCliente.setText("");
        
        jButtonEditarCliente.setEnabled(false);
        jButtonCancelarCliente.setEnabled(false);
        jButtonSalvarCliente.setEnabled(false);
        
        jTextFieldCodigoCliente.setEnabled(false);
        jTextFieldContactoCliente.setEnabled(false);
        jTextFieldIdadeCliente.setEnabled(false);
        jTextFieldMoradaCliente.setEnabled(false);
        jTextFieldNomeCliente.setEnabled(false);
        jTextFieldPesquisaCliente.setEnabled(true);
        jComboBoxSexoCliente.setEnabled(false);
        
        preencherTabela("select * from clientes order by id");
        
        }else{
            
            cliente.setCodigo(Integer.parseInt(jTextFieldCodigoCliente.getText()));
            cliente.setContacto(jTextFieldContactoCliente.getText());
            cliente.setIdade(Integer.parseInt(jTextFieldIdadeCliente.getText()));
            cliente.setMorada(jTextFieldMoradaCliente.getText());
            cliente.setNome(jTextFieldNomeCliente.getText());
            cliente.setSexo((String)jComboBoxSexoCliente.getSelectedItem());
            control.editar(cliente);
            
            
                jButtonSalvarCliente.setEnabled(false);
                
                jTextFieldCodigoCliente.setEnabled(false);
                jTextFieldContactoCliente.setEnabled(false);
                jTextFieldIdadeCliente.setEnabled(false);
                jTextFieldMoradaCliente.setEnabled(false);
                jTextFieldNomeCliente.setEnabled(false);
                jTextFieldPesquisaCliente.setEnabled(true);
                jComboBoxSexoCliente.setEnabled(false);
                preencherTabela("select * from clientes order by id");
       }
    }//GEN-LAST:event_jButtonSalvarClienteActionPerformed

