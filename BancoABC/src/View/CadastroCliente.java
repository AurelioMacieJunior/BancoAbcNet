    private void jTableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClienteMouseClicked
        String nome = ""+jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from clientes where nome='"+nome+"'");
            try {
                conex.rs.first();
                jTextFieldCodigoCliente.setText(String.valueOf(conex.rs.getInt("id")));
                jTextFieldNomeCliente.setText(conex.rs.getString("nome"));
                jTextFieldContactoCliente.setText(conex.rs.getString("contacto"));
                jTextFieldIdadeCliente.setText(String.valueOf(conex.rs.getInt("idade")));
                jComboBoxSexoCliente.setSelectedItem(String.valueOf(conex.rs.getString("sexo")));
                jTextFieldMoradaCliente.setText(conex.rs.getString("morada"));
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERRO NA CAPTURA DE DADOS!\n ERROR: "+ex);
            }
        
        conex.desconexao();
        jButtonEditarCliente.setEnabled(true);
        jButtonExcluirCliente.setEnabled(true);
    }//GEN-LAST:event_jTableClienteMouseClicked

