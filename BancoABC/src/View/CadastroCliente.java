    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String[]{"id", "nome", "contacto", "idade", "sexo", "morada"};
        conex.conexao();
        
        conex.executaSql(sql);
            try {
                conex.rs.first();
                do{
                  dados.add(new Object[]{conex.rs.getInt("id"),conex.rs.getString("nome"),conex.rs.getString("contacto"),conex.rs.getInt("idade"),conex.rs.getString("sexo"),conex.rs.getString("morada")});
                }while(conex.rs.next());
            } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "REALIZE NOVAMENTE A BUSCA!");
            }
            
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableCliente.setModel(modelo);
        jTableCliente.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTableCliente.getColumnModel().getColumn(0).setResizable(false);
        jTableCliente.getColumnModel().getColumn(1).setPreferredWidth(170);
        jTableCliente.getColumnModel().getColumn(1).setResizable(false);
        jTableCliente.getColumnModel().getColumn(2).setPreferredWidth(170);
        jTableCliente.getColumnModel().getColumn(2).setResizable(false);
        jTableCliente.getColumnModel().getColumn(3).setPreferredWidth(70);
        jTableCliente.getColumnModel().getColumn(3).setResizable(false);
        jTableCliente.getColumnModel().getColumn(4).setPreferredWidth(70);
        jTableCliente.getColumnModel().getColumn(4).setResizable(false);
        jTableCliente.getColumnModel().getColumn(5).setPreferredWidth(170);
        jTableCliente.getColumnModel().getColumn(5).setResizable(false);
        jTableCliente.getTableHeader().setReorderingAllowed(false);
        jTableCliente.setAutoResizeMode(jTableCliente.AUTO_RESIZE_OFF);
        jTableCliente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconexao();
    }
    
